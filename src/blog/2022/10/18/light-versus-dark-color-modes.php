<p>Basicly, it is a personal preference of whether you want to see more white and colors closer to white, or more black and colors closer to black.</p>

<p>It doesn't matter as much for physical objects, but you may have a preference for those too, it matters more for digital devices with some kind of display.</p>

<h2>Why should you care whether you are in light mode or dark mode?</h2>

<p>Good question. As it is personal preference, everyone will be different as to which one they prefer, but if you haven't made up your mind yet, here are some things to think about.</p>

<h3>The time of day and brightness of your environment may matter.</h3>

<p>During the day, or while in brighter environments, you may wish to use light mode, as it would fit in more with the surroundings.</p>

<p>At night, or while in darker environments, you may wish to use dark mode, as it would not cause your device or screen to shine so brightly and light up the room.</p>

<h3>Dark mode may be easier on the eyes.</h3>

<p>Your eyes adjust to the brightness of the colors on the screen. Going from brighter surroundings to a darker screen is easy, however, the opposite, going from a dark environment to a brighter screen is harder and can more easily cause strain on your eyes.</p>

<p>Have you ever tried to check the time on your phone in the middle of the night as you turn over in your bed? Your bedroom would most likely be really dark with very little light, and the bright white screen of your phone may be hard to read.</p>

<p>Brighter colors have been known to keep you awake for longer, or interfere with your body's natural winding down to sleep, which can cause you to get less sleep during the night, and possibly make you wake up later the following day, that has its own problems. Dark mode allows your body to continue winding down at night and hopefully sleep well.</p>

<h3>Dark mode may use less power.</h3>

<p>This is especially important for your phones and tablets, but applies to all devices. Using less energy to power your devices has many benefits.</p>

<ul>
    <li>It may cost less for the same amount of usage over light mode.</li>
    <li>Battery powered devices may last longer before needing a recharge or replacement of batteries.</li>
    <li>More devices could potentially be powered at the same time for the same cost over using light mode.</li>
</ul>

<h2>Conclusion</h2>

<p>Use this article to help guide you to making your own personal decision. It would be impossible for me to tell you which one is best for you, as it would depend on other factors that form part of your life, I can only guide you in the hopes the decision you make is right for you.</p>

<p>Try changing your device's display settings, and switch it to the opposite mode to what you have now, then return to this article. It will change automatically depending on your preference, giving you a real example of how other things could be affected by your preference.</p>

